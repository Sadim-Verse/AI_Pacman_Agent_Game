class ExactInference(InferenceModule):
    """
    The exact dynamic inference module should use forward algorithm updates to
    compute the exact belief function at each time step.
    """
    def initializeUniformly(self, gameState):
        """
        Begin with a uniform distribution over legal ghost positions (i.e., not
        including the jail position).
        """
        self.beliefs = DiscreteDistribution()
        for p in self.legalPositions:
            self.beliefs[p] = 1.0
        self.beliefs.normalize()

    def observeUpdate(self, observation, gameState):
        """
        Update beliefs based on the distance observation and Pacman's position.

        The observation is the noisy Manhattan distance to the ghost you are
        tracking.

        self.allPositions is a list of the possible ghost positions, including
        the jail position. You should only consider positions that are in
        self.allPositions.

        The update model is not entirely stationary: it may depend on Pacman's
        current position. However, this is not a problem, as Pacman's current
        position is known.
        """
        "*** YOUR CODE HERE ***"
        "Q3"

        # we are updating self.beliefs using inference, and then normalizing

        pos_count = len(self.allPositions)

        # iterate through all positions
        for i in range(pos_count):

            # set curr_ghost to the tuple representing its hypothetical position
            curr_ghost = self.allPositions[i]

            # update the belief about the current ghost given the observation
            self.beliefs[curr_ghost] = self.beliefs[curr_ghost] * self.getObservationProb(observation,
                                                                                          gameState.getPacmanPosition(),
                                                                                          curr_ghost,
                                                                                          self.getJailPosition())
        self.beliefs.normalize()

